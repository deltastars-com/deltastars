// =====================================================
// Netlify AI Validator Plugin
// يقوم بفحص الملفات وتصحيح الأخطاء قبل النشر
// =====================================================

module.exports = {
  name: 'netlify-plugin-ai-validator',
  
  async onPreBuild({ utils, constants }) {
    console.log('🤖 AI Agent: Starting pre-build validation...');
    
    // فحص وجود الملفات الأساسية
    const requiredFiles = [
      'package.json',
      'index.html',
      'src/main.tsx',
      'src/App.tsx',
      'src/lib/supabaseClient.ts'
    ];
    
    for (const file of requiredFiles) {
      const exists = await utils.build.fileExists(file);
      if (!exists) {
        console.log(`⚠️ AI Agent: Missing required file: ${file}`);
        // محاولة إنشاء الملف تلقائياً
        await utils.build.createFile(file, '// Auto-generated by AI Agent');
      } else {
        console.log(`✅ AI Agent: Found ${file}`);
      }
    }
    
    // فحص package.json من الأخطاء الشائعة
    const pkg = await utils.build.readPackageJson();
    let modified = false;
    
    // إزالة الحزم غير الضرورية
    const invalidDeps = ['prisma', '@prisma/client', 'express', 'cors', 'server.ts'];
    for (const dep of invalidDeps) {
      if (pkg.dependencies?.[dep]) {
        delete pkg.dependencies[dep];
        modified = true;
        console.log(`🗑️ AI Agent: Removed invalid dependency: ${dep}`);
      }
      if (pkg.devDependencies?.[dep]) {
        delete pkg.devDependencies[dep];
        modified = true;
        console.log(`🗑️ AI Agent: Removed invalid devDependency: ${dep}`);
      }
    }
    
    // التأكد من وجود scripts الأساسية
    if (!pkg.scripts?.build) {
      pkg.scripts = pkg.scripts || {};
      pkg.scripts.build = 'tsc && vite build';
      modified = true;
      console.log('📝 AI Agent: Added build script');
    }
    
    if (modified) {
      await utils.build.writePackageJson(pkg);
      console.log('💾 AI Agent: Saved corrected package.json');
    }
    
    // فحص متغيرات البيئة
    const envVars = constants.ENVIRONMENT_VARIABLES || {};
    const requiredEnv = ['VITE_SUPABASE_URL', 'VITE_SUPABASE_ANON_KEY'];
    
    for (const env of requiredEnv) {
      if (!envVars[env]) {
        console.log(`⚠️ AI Agent: Missing environment variable: ${env}`);
      } else {
        console.log(`✅ AI Agent: Found ${env}`);
      }
    }
    
    console.log('🤖 AI Agent: Pre-build validation completed!');
  },
  
  async onBuild({ utils }) {
    console.log('🤖 AI Agent: Starting build validation...');
    
    // فحص وجود مجلد dist بعد البناء
    const distExists = await utils.build.fileExists('dist');
    if (!distExists) {
      console.log('❌ AI Agent: Build failed - dist folder not found');
      throw new Error('Build failed: dist folder missing');
    }
    
    // فحص وجود index.html في dist
    const indexPath = 'dist/index.html';
    const indexExists = await utils.build.fileExists(indexPath);
    if (!indexExists) {
      console.log('❌ AI Agent: Build failed - index.html not found in dist');
      throw new Error('Build failed: index.html missing');
    }
    
    console.log('✅ AI Agent: Build validation passed!');
  },
  
  async onPostBuild({ utils }) {
    console.log('🤖 AI Agent: Post-build cleanup...');
    
    // تنظيف الملفات المؤقتة
    const tempFiles = ['.cache', '.netlify', 'node_modules/.cache'];
    for (const file of tempFiles) {
      if (await utils.build.fileExists(file)) {
        await utils.build.deleteFile(file);
        console.log(`🧹 AI Agent: Cleaned ${file}`);
      }
    }
    
    console.log('✅ AI Agent: Deploy ready!');
  }
};
module.exports = {
  name: 'netlify-plugin-ai-validator',
  async onPreBuild({ utils }) {
    console.log('🤖 AI Agent: Validating files...');
    const pkg = await utils.build.readPackageJson();
    let modified = false;
    const invalidDeps = ['prisma', '@prisma/client', 'express', 'cors'];
    for (const dep of invalidDeps) {
      if (pkg.dependencies?.[dep]) {
        delete pkg.dependencies[dep];
        modified = true;
      }
    }
    if (modified) await utils.build.writePackageJson(pkg);
    console.log('✅ AI Agent: Validation complete');
  }
};
